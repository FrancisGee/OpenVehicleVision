files = Dataset.filenames('*.tif');

idx = 0;
appendfiles = cell(1,numel(files));

for n = 1:numel(files)
    % files are ordered by name.
    gtfilename = files{n};
    
    % gtfilename(end-2:end) = 'png'; % After-Rain
    gtfilename = [gtfilename(1:end-3) '.png']; % Sunny-Shadows
    
    if exist(gtfilename, 'file') ~= 2  % file not exist
        lastgtfilename = files{n-1};
        
        %lastgtfilename(end-2:end) = 'png'; % After-Rain
        lastgtfilename = [lastgtfilename(1:end-3) '.png']; % Sunny-Shadows
        
        imwrite(imread(lastgtfilename), gtfilename);
        disp(gtfilename);
        
        idx = idx+1;
        appendfiles{idx} = gtfilename;
    end
end

disp ok;

